#include <gtest/gtest.h>

#if !defined(_WIN32) && !defined(__ANDROID__)

#include <quick_ai_test_utils.h>

#include <multilingual_tinybert_16mb.h>

#include <memory>

namespace {

/**
 * @brief Tiny MultilingualTinyBert differential adapter
 *
 * BertTransformer sanitizes its config before initializing the virtual
 * Transformer base, so this thin subclass owns that mem-initializer.
 */
class TinyBertRefAdapter final
  : public quick_ai_test::EmbeddingTestAdapter<quick_ai::MultilingualTinyBert> {
public:
  TinyBertRefAdapter(quick_ai::json &cfg, quick_ai::json &generation_cfg,
                     quick_ai::json &nntr_cfg) :
    quick_ai::Transformer(sanitizeConfig(cfg), generation_cfg, nntr_cfg,
                          quick_ai::ModelType::EMBEDDING),
    quick_ai_test::EmbeddingTestAdapter<quick_ai::MultilingualTinyBert>(
      cfg, generation_cfg, nntr_cfg) {}
};

/**
 * @brief Differential model descriptor for the tiny MultilingualTinyBert
 * fixture
 */
quick_ai_test::DifferentialModel tinyBertModel() {
  return {
    "tinybert_tiny",
    [](quick_ai::json &cfg, quick_ai::json &gen_cfg, quick_ai::json &nntr_cfg) {
      return std::make_unique<TinyBertRefAdapter>(cfg, gen_cfg, nntr_cfg);
    },
  };
}

/**
 * @brief FP32 raw encoder output matches the HF BertModel reference
 */
TEST(TinyBertDifferentialTest, FP32MatchesHFReference) {
  quick_ai_test::runFp32EmbeddingDifferentialChecks(tinyBertModel());
}

/**
 * @brief Q4_0 encoder output is close to the HF FP32 reference
 *        (skips automatically if nntr_quantize does not support
 * BertForMaskedLM)
 */
TEST(TinyBertDifferentialTest, Q40CloseToFP32Reference) {
  quick_ai_test::runQ40EmbeddingDifferentialChecks(tinyBertModel());
}

} // namespace

#endif // !_WIN32 && !__ANDROID__
