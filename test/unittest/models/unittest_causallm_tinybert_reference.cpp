#include <gtest/gtest.h>

#if !defined(_WIN32) && !defined(__ANDROID__)

#include <causallm_test_utils.h>

#include <multilingual_tinybert_16mb.h>

#include <memory>

namespace {

/**
 * @brief Tiny MultilingualTinyBert differential adapter
 *
 * BertTransformer sanitizes its config before initializing the virtual
 * Transformer base, so this thin subclass owns that mem-initializer.
 */
class TinyBertRefAdapter final
  : public causallm_test::EmbeddingTestAdapter<causallm::MultilingualTinyBert> {
public:
  TinyBertRefAdapter(causallm::json &cfg, causallm::json &generation_cfg,
                     causallm::json &nntr_cfg) :
    causallm::Transformer(sanitizeConfig(cfg), generation_cfg, nntr_cfg,
                          causallm::ModelType::EMBEDDING),
    causallm_test::EmbeddingTestAdapter<causallm::MultilingualTinyBert>(
      cfg, generation_cfg, nntr_cfg) {}
};

/**
 * @brief Differential model descriptor for the tiny MultilingualTinyBert fixture
 */
causallm_test::DifferentialModel tinyBertModel() {
  return {
    "tinybert_tiny",
    [](causallm::json &cfg, causallm::json &gen_cfg, causallm::json &nntr_cfg) {
      return std::make_unique<TinyBertRefAdapter>(cfg, gen_cfg, nntr_cfg);
    },
  };
}

/**
 * @brief FP32 raw encoder output matches the HF BertModel reference
 */
TEST(TinyBertDifferentialTest, FP32MatchesHFReference) {
  causallm_test::runFp32EmbeddingDifferentialChecks(tinyBertModel());
}

} // namespace

#endif // !_WIN32 && !__ANDROID__
